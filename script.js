function applyLang(lang){
  const dict = I18N[lang] || I18N.fr;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el=>{
    const k = el.getAttribute("data-i18n"); if(dict[k] != null) el.innerHTML = dict[k];
  });
  document.querySelectorAll("[data-i18n-ph]").forEach(el=>{
    const k = el.getAttribute("data-i18n-ph"); if(dict[k] != null) el.setAttribute("placeholder", dict[k]);
  });
  const meta = document.querySelector('meta[name="description"]');
  if(meta && dict["meta.desc"]) meta.setAttribute("content", dict["meta.desc"]);
  document.querySelectorAll(".lang-switch button").forEach(b=> b.classList.toggle("active", b.dataset.lang === lang));
  splitMark();
  try{ localStorage.setItem("pulse-lang", lang); }catch(e){}
}

// Wrap each letter of "votre marque" so the outline can be re-drawn letter by letter
function brandGradColor(t){
  const stops=[[192,136,92,0],[176,142,116,.5],[154,134,107,1]]; // #c0885c → #b08e74 → #9a866b
  let a=stops[0], b=stops[1];
  if(t>0.5){ a=stops[1]; b=stops[2]; }
  const seg=(t-a[3])/(b[3]-a[3]);
  return `rgb(${Math.round(a[0]+(b[0]-a[0])*seg)},${Math.round(a[1]+(b[1]-a[1])*seg)},${Math.round(a[2]+(b[2]-a[2])*seg)})`;
}
function splitMark(){
  const el = document.querySelector(".tl-mark");
  if(!el) return;
  const text = el.textContent;
  const n = text.length;
  el.textContent = "";
  [...text].forEach((ch,i)=>{
    const s = document.createElement("span");
    s.className = "ol-l";
    s.style.setProperty("--i", i);
    s.style.setProperty("--c", brandGradColor(n>1 ? i/(n-1) : 0));
    s.textContent = ch === " " ? " " : ch;
    el.appendChild(s);
  });
}
