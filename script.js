function animateCount(el){
  const target=parseFloat(el.dataset.target);
  const decimals=el.dataset.target.indexOf(".")>-1?1:0;
  const dur=1500,t0=performance.now();
  (function tick(now){
    const p=Math.min((now-t0)/dur,1), eased=1-Math.pow(1-p,3);
    el.textContent=(target*eased).toFixed(decimals);
    if(p<1) requestAnimationFrame(tick); else el.textContent=target.toFixed(decimals);
  })(t0);
}
const countIO=new IntersectionObserver((entries)=>{
  entries.forEach(e=>{ if(e.isIntersecting){ animateCount(e.target); countIO.unobserve(e.target); } });
},{threshold:.6});
document.querySelectorAll(".count").forEach(el=> countIO.observe(el));

